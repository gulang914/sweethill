    {
        $cate = new Cate();
        $cates = $cate->get();
        foreach($cates as $k => $v){
            $n = substr_count($v['path'],',');
            $cates[$k]['cname'] = str_repeat('|----',$n).$cates[$k]['cname'];
        };

        return view('admin.cate.index',compact('cates'));
    }

